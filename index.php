 <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#FFFFFF"><b>New Question & Answer session with Jonny Watts, Producer of RollerCoaster Tycoon 3<sup>®</sup> at Frontier Developments.
</b> <br><br>
          <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">
		<b><font color="#FFFF00">Q: How is Chris Sawyer involved in the development of RollerCoaster Tycoon 3 (RCT3)?</b></font><br>
<b>A:</b> Frontier has a longstanding relationship with Chris Sawyer as we mentioned in previous articles.  Chris is still very much involved in the development of the game, just in a different role - rather than doing all the work himself he is taking the overview, making sure we stay true to the essence of what makes RCT great - but at the same time allowing our own creative energy, as huge fans of the game in our own right, to help take it forward in this exciting new direction.<br><br>
<b><font color="#FFFF00">Q: What are the minimum system requirements to run RCT3 and how does this compare to other PC games?</font></b><br>
<b>A:</b> <br>Pentium(r) III 733 MHz Processor<br>
128 MB RAM<br>
250 MB hard drive space<br>
32MB video card with hardware T&L (Such as nVidia GeForce2 or higher or any ATI Radeon)<br><br>
 
The original RCT game had a low minimum spec and as such was able to successfully appeal to a very wide audience, many of whom who do not necessarily choose to stay on the bleeding edge of PC technology.  Equally, that game could make even high-end PCs struggle if the player put a whole bunch of content in a large park.  
 
Similarly with the new 3D release, we have tried to ensure that the game can be enjoyed by as many people as possible on a wide range of PCs.  Our minimum spec is pretty low compared to other 3D PC games, especially ones targeted at 'core gamers'.  We have worked very hard at keeping the performance up on low spec machines, as well as making the game visually very attractive on all machines without necessarily relying on very performance-hungry graphical techniques.  So our intention is that players will be able to enjoy the game on the minimum spec machine, but to experience the game looking & running at its absolute best higher end PCs / graphics cards will inevitably be needed.
 
Even though the individual components of RCT3 can be quite simple, the sheer complexity of the game including the number of people, rides moving in real time, and other special effects, makes it an impressive technological and artistic achievement compared to other games that may use more polygons on each component, but offer nothing like the richness and diversity of RCT3.  And yes, with the ability to build large parks that are rich in animated content, have literally thousands of truly individual park guests all interacting with the park and each other, AND ride the most intense Rollercoaster rides your imagination can conjure up you will still be able to make even the top spec systems struggle if you really want to!
<br><br>
<b><font color="#FFFF00">Q: Will RCT3 have a sandbox mode?  How is this implemented?</font> </b><br>
<b>A:</b> Yes (cue the sound of cheers from around the world..)!  Very simple - you can put anything you like into your park, with the one caveat that a small amount of content is unlock able throughout the game.  So straight off the bat you'll be able to use pretty much everything in your park, but to get a truly complete-ist buzz you have some serious Tycoon-ing to do!<br><br>
<b><font color="#FFFF00">Q: How many rides are there going to be in RCT3?</font></b><br>
<b>A:</b> Broadly speaking for this first release we set out to deliver around 75% of the 'content' (rides and scenery) that were in RCT2 – however, our enthusiasm has got the better of us and we'll have probably done a bit more than that by the time we're finished.<br><br>
<b><font color="#FFFF00">Q: How is “theming” and ride customization handled in RCT3?</font></b> <br>
<b>A:</b> The way we've been building the rides is to produce a "vanilla" version of a ride and then 'theme' it, so as a general rule there are multiple versions of each ride available, themed and not themed.  There is not a completely rigorous approach whereby each ride has an alternate version in every theme, but we've done the best we can given the time and people available.  Also we've tried to ensure that the themes we do are relatively rich compared to some in previous incarnations of the game, so as well as rides & attractions there are always also building sets, staff uniforms, foliage, benches, lamps and so on.  We have also tried to incorporate this into the gameplay in subtle ways, so that now, for example, a Peeps impression of a park is partly based on the coherency of the themeing.<br><br>
		</p></font>        
